//! "Delete browsing data" — Brave's destructive privacy action, adapted for
//! offline use while Brave is closed.
//!
//! A `DataCategory` lists exactly which files and directories back a UI option
//! on `brave://settings/privacy/delete-browsing-data`. The tool never touches
//! anything outside these lists. Paths ending with `/` are directories whose
//! *contents* are removed (the directory itself stays in place so Brave can
//! recreate files on next launch without running into permission surprises).
//!
//! Every category is conservative — it deliberately leaves state Brave cannot
//! regenerate alone. In particular these are NEVER in any category:
//!
//!   - `Bookmarks`, `Bookmarks.bak`       (user content)
//!   - `Preferences`, `Secure Preferences` (shield + privacy settings)
//!   - `Extensions/`, `Local Extension Settings/` (installed extensions)
//!   - `Sync Data/`                        (sync handshake state)
//!
//! On Linux some paths live under `~/.cache/BraveSoftware/...` rather than the
//! config dir, so the clear/size functions try both the profile dir and the
//! channel's `cache_root/<profile>/` for each target.

use std::path::{Path, PathBuf};

