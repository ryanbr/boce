#[derive(Debug, Default)]
pub struct ClearReport {
    pub touched_paths: Vec<PathBuf>,
    pub skipped: Vec<(PathBuf, String)>,
    pub bytes_freed: u64,
}

/// Dry-run size computation for a category. Walks every target and sums the
/// on-disk bytes; zero if none of the targets exist.
pub fn size_of(paths: &ChannelPaths, profile: &Profile, cat: &DataCategory) -> u64 {
    let mut total = 0;
    for tgt in cat.targets {
        for root in candidate_roots(paths, profile) {
            total += target_size(&root, tgt);
        }
    }
    total
}

/// Delete the category's targets. On `dry_run`, only sizes are computed.
pub fn clear(
    paths: &ChannelPaths,
    profile: &Profile,
    cat: &DataCategory,
    dry_run: bool,
) -> Result<ClearReport> {
    let mut report = ClearReport::default();
    // Some targets live under the profile dir on Windows/macOS but under a
    // separate cache root on Linux; try both and dedupe.
    let roots = candidate_roots(paths, profile);

    for tgt in cat.targets {
        let trimmed = tgt.trim_end_matches('/');
        let is_dir_contents = tgt.ends_with('/');
        let mut done_paths: Vec<PathBuf> = Vec::new();
        for root in &roots {
            let p = root.join(trimmed);
            // Skip dupes across identical roots (Windows/macOS case).
            if done_paths.contains(&p) {
                continue;
            }
            done_paths.push(p.clone());
            if !p.exists() {
                continue;
            }
            let size = target_size(root, tgt);
            if dry_run {
                report.touched_paths.push(p);
                report.bytes_freed += size;
                continue;
            }
            let res = if is_dir_contents || p.is_dir() {
                clear_dir_contents(&p)
            } else {
                std::fs::remove_file(&p).with_context(|| format!("removing {}", p.display()))
            };
            match res {
                Ok(()) => {
                    report.touched_paths.push(p);
                    report.bytes_freed += size;
                }
                Err(e) => report.skipped.push((p, e.to_string())),
            }
        }
    }
    Ok(report)
}

fn candidate_roots(paths: &ChannelPaths, profile: &Profile) -> Vec<PathBuf> {
    let mut roots = vec![profile.path.clone()];
    let cache_profile = paths.cache_root.join(&profile.dir_name);
    if cache_profile != profile.path {
        roots.push(cache_profile);
    }
    roots
}

fn target_size(base: &Path, tgt: &str) -> u64 {
    let trimmed = tgt.trim_end_matches('/');
    let p = base.join(trimmed);
    if !p.exists() {
        return 0;
    }
    if p.is_file() {
        std::fs::metadata(&p).map(|m| m.len()).unwrap_or(0)
    } else {
        walkdir::WalkDir::new(&p)
            .into_iter()
            .filter_map(|e| e.ok())
            .filter(|e| e.file_type().is_file())
            .filter_map(|e| e.metadata().ok())
            .map(|m| m.len())
            .sum()
    }
}

fn clear_dir_contents(dir: &Path) -> Result<()> {
    for entry in std::fs::read_dir(dir)
        .with_context(|| format!("reading {}", dir.display()))?
    {
        let entry = entry?;
        let p = entry.path();
        if entry.file_type()?.is_dir() {
            std::fs::remove_dir_all(&p)
                .with_context(|| format!("removing dir {}", p.display()))?;
        } else {
            std::fs::remove_file(&p)
                .with_context(|| format!("removing file {}", p.display()))?;
        }
    }
    Ok(())
}

pub fn lookup(id: &str) -> Option<&'static DataCategory> {
    CATEGORIES.iter().find(|c| c.id.eq_ignore_ascii_case(id))
}

/// Friendly byte-size label: 0 B / 12.34 MiB / etc.
pub fn format_bytes(n: u64) -> String {
    const UNITS: &[&str] = &["B", "KiB", "MiB", "GiB", "TiB"];
    let mut v = n as f64;
    let mut i = 0;
    while v >= 1024.0 && i < UNITS.len() - 1 {
        v /= 1024.0;
        i += 1;
    }
    if i == 0 {
        format!("{} {}", n, UNITS[i])
    } else {
        format!("{:.2} {}", v, UNITS[i])
    }
}
